def numeric(form,field):
    if type(field.data)==str:
        raise ValidationError('this field must be an integer')

def phonic(form,field):
    x=field.data.lower()
    if x.islower():
        raise ValidationError('must not contain letters ')
    if(len(field.data) < 9 ):
        raise ValidationError('invalid mobile number must be greater than 9 digits')
    
    
def facebook(form,field):
    if not field.data.startswith('https://www.facebook.com/'):
        raise ValidationError('not a valid account')
class ShowForm(Form):
    artist_id = IntegerField(
        'artist_id',
        validators=[DataRequired(), numeric]
    )
    venue_id = IntegerField(
        'venue_id',
        validators=[DataRequired(), numeric]
    )
    start_time = DateTimeField(
        'start_time',
        validators=[DataRequired()],
        default= datetime.today()
    )

